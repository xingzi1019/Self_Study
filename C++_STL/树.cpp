//树的遍历
//树的遍历就是不重不漏的将树中所有的点都扫描⼀遍。
//在之前学过的线性结构中 遍历就很容易 直接从前往后扫描⼀遍即可
//但是在树形结构中 如果不按照⼀定的规则遍历 就会漏掉或者重复遍历⼀些结点
//因此 在树形结构中 要按照⼀定规则去遍历
//常⽤的遍历⽅式有两种 ⼀种是深度优先遍历 另⼀种是宽度优先遍历
// 
//							注意：树的遍历是许多关于树的算法的基⽯，⼀定要重点掌握
// 
//深度优先遍历 --- DFS
//英⽂缩写为 DFS，全称是 Depth First Search 中⽂名是深度优先搜索 是⼀种⽤于遍历或搜索树或图的算法
//所谓深度优先 就是说每次都尝试向更深的节点⾛ 也就是⼀条路⾛到黑 
//具体流程：
//1. 从根节点出发，依次遍历每⼀棵⼦树；
//2. 遍历⼦树的时候，重复第⼀步。
//因此，深度优先遍历是⼀种递归形式的遍历，可以⽤递归来实现
//从根结点开始 找到一个没有访问过的孩子 以孩子为根  -----> 重复做这个事 那就可以用递归来实现
//因此 DFS 就是利用树的递归定义 使用递归的形式进行遍历

