//满二叉树的一些性质:
//1.高度为 h 的满二叉树 结点个数为 2^h - 1 
//2.结点为 n 的满二叉树 书高 h 为 log2(n + 1)  ----> 用性质一可推导
//3.如果将满二叉树按照层序遍历的过程编号:从根结点开始,由 1 开始编号
