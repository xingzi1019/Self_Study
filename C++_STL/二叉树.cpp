//------> 结点 i 的右孩子的编号为 2 * i + 1 
//------> 结点 i 双亲的编号为 i / 2
//上面的性质有利于存储二叉树

//完全⼆叉树
//对⼀棵树有 n 个结点的⼆叉树按层序编号，所有的结点的编号从 1 ~ n 
//如果这棵树所有结点和同样深度的满⼆叉树的编号为从 1~ n 的结点位置相同 则这棵⼆叉树为完全⼆叉树
//说⽩了 就是在满⼆叉树的基础上 在最后⼀层的叶⼦结点上 从右往左依次删除若⼲个结点 剩下的就是⼀棵完全⼆叉树
